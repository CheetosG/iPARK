// backend/server.js
/**
 * iPark Backend Server
 * 
 * This is the main entry point for the iPark Node.js/Express server.
 * It handles:
 * - Express server configuration and middleware
 * - MongoDB connection
 * - RESTful API routes
 * - Real-time communication via Socket.IO
 * - Background maintenance tasks (Spot status synchronization)
 */

require('dotenv').config();
const express = require('express');
const http = require('http');
const { Server } = require('socket.io');
const mongoose = require('mongoose');
const cors = require('cors');
const helmet = require('helmet');
const rateLimit = require('express-rate-limit');
const connectDB = require('./config/db');
const authRoutes = require('./routes/auth');
const mallRoutes = require('./routes/mall');
const reservationRoutes = require('./routes/reservation');
const adminRoutes = require('./routes/admin');
const userRoutes = require('./routes/user'); 
const chatRoutes = require('./routes/chat');
const errorHandler = require('./middleware/errorHandler');
const ErrorLog = require('./models/ErrorLog');
const catchAsync = require('./utils/catchAsync');
const AppError = require('./utils/appError');
const path = require('path');

const app = express();
const server = http.createServer(app);
const io = new Server(server, { cors: { origin: "*" } });

const iotManager = require('./utils/iotManager');
iotManager.init(io);

// Attach Socket.IO and IoT manager instances to the app for use in route handlers
app.set('io', io);
app.set('iotManager', iotManager);
app.set('trust proxy', 1);

// Route WebSocket upgrades on /ws/iot to the iotManager
server.on('upgrade', (request, socket, head) => {
  const pathname = request.url.split('?')[0];
  if (pathname === '/ws/iot') {
    iotManager.handleUpgrade(request, socket, head);
  }
});

// --- Middleware Stack ---

app.use(helmet()); // Basic security headers
app.use(cors());   // Enable Cross-Origin Resource Sharing
app.use(express.json()); // Parse JSON bodies
app.use(express.urlencoded({ extended: true }));

// Serve static files (like profile pictures) from the 'uploads' directory
app.use('/uploads', express.static(path.join(__dirname, 'uploads')));

// Rate Limiting: Prevents brute-force attacks by limiting requests per IP
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 100, // Limit each IP to 100 requests per window
  message: {
    success: false,
    message: 'Too many requests from this IP, please try again after 15 minutes'
  },
  standardHeaders: true,
  legacyHeaders: false,
});
app.use('/api/', limiter);

// Initialize Database Connection
connectDB();

// --- REST API Routes ---

app.use('/api/auth', authRoutes);         // Login, Register, Profile
app.use('/api/mall', mallRoutes);         // Mall listing and info
app.use('/api/reservation', reservationRoutes); // Booking and status updates
app.use('/api/admin', adminRoutes);       // Dashboard and system management
app.use('/api/user', userRoutes);         // User settings and lists
app.use('/api/chat', chatRoutes);         // Support chat history

// --- Real-time Socket.IO Logic ---

io.on('connection', (socket) => {
  console.log('User connected:', socket.id);

  /**
   * Support Chat: Join a dedicated room for the user.
   * Sends an automated greeting if the user is starting a new session.
   */
  socket.on('join_chat', async (data) => {
    const { userId, userName } = data;
    if (!userId) return;
    const room = userId.toString();
    socket.join(room);
    console.log(`User ${userName} (${room}) joined support chat.`);

    const ChatMessage = require('./models/ChatMessage');
    const lastMsg = await ChatMessage.findOne({ userId }).sort({ createdAt: -1 });
    
    // Auto-greeting logic: Only send if no previous messages OR last message is > 1 hour old
    const oneHourAgo = new Date(Date.now() - 3600000);
    const shouldSendGreeting = !lastMsg || new Date(lastMsg.createdAt) < oneHourAgo;

    if (shouldSendGreeting) {
      const automatedMsg = {
        userId: userId,
        senderId: '000000000000000000000001', // Virtual Support System ID
        senderName: 'Sarah from iPark Support',
        message: `Hi ${userName}, welcome back to iPark support! How can we help you today?`,
        isAdmin: true,
        isAutoGenerated: true,
        createdAt: new Date()
      };
      
      const savedMsg = await ChatMessage.create(automatedMsg);
      io.to(userId).emit('receive_chat_message', savedMsg.toJSON());
    }
  });

  /**
   * Identifies the socket with a User ID.
   * Allows the server to target specific users for notifications.
   */
  socket.on('identify_user', (data) => {
    const { userId } = data;
    if (userId) {
      socket.join(userId.toString());
      console.log(`Socket ${socket.id} identified as user ${userId}`);
    }
  });

  /**
   * Forwards a chat message to the appropriate room.
   */
  socket.on('send_chat_message', async (data) => {
    try {
      const ChatMessage = require('./models/ChatMessage');
      const newMessage = await ChatMessage.create(data);
      const plainMsg = newMessage.toJSON();
      
      const room = data.userId.toString();
      io.to(room).emit('receive_chat_message', plainMsg);

      // --- ADMIN / SUPPORT NOTIFICATION ---
      // If the message is from a regular user (not an admin), alert the staff
      if (!data.isAdmin && !data.isAutoGenerated) {
        const User = require('./models/User');
        const staffUsers = await User.find({ role: { $in: ['admin', 'support'] } });
        staffUsers.forEach(staff => {
          io.to(staff._id.toString()).emit('admin_support_alert', {
            type: 'chat',
            title: 'New Chat Message',
            body: `${data.senderName || 'A user'} sent a message: ${data.message}`,
            data: plainMsg
          });
        });
      }
    } catch (err) {
      console.error('Socket error in send_chat_message:', err);
    }
  });

  socket.on('disconnect', () => {
    console.log('User disconnected:', socket.id);
  });
});

