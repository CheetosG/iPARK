/**
 * Frontend Error Reporting: Allows the Flutter app to log crashes/errors to the server.
 * Triggers a real-time alert for admins.
 */
app.post('/api/admin/report-client-error', catchAsync(async (req, res) => {
  const { errorCode, message, stack, metadata } = req.body;
  const log = await ErrorLog.create({
    errorCode: errorCode || 'FE-GEN-001',
    message: message || 'Unknown frontend error',
    stack,
    path: 'Frontend/App',
    userId: req.user ? req.user.id : null,
    metadata
  });
  
  const io = req.app.get('io');
  if (io) {
    io.emit('system_error_alert', {
      logId: log._id,
      errorCode: errorCode || 'FE-GEN-001',
      message: message || 'Frontend Crash Detected',
      path: 'Frontend',
      timestamp: new Date()
    });
  }

  res.json({ success: true });
}));

